## Receiving User Input and Calculating their future age.

# Ask for current age

current_age = int(input("How old are you? "))

# Years until 2050

years_until_2050 = 2050 - 2023


# Calculations for age

age = current_age + years_until_2050

# Results

print(f"In 2050, you will be {age} years old.")
